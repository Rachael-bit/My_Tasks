# Upper()
name = "Ada Balogun"
print(name.upper())

# Lower()
name = "Python is Amazing"
print(name.lower())

# Strip()
text = "    Abuja   "
print(text.strip())

# Replace
message = "I love Java"
print(message.replace("Java", "Python"))

#lstrip() Removes whitespace (or specified characters) from the left side only.
text = "    Nigeria"
print(text.lstrip())

#rstrip Removes whitespace (or specified characters) from the right side only.
text = "Nigeria     "
print(text.rstrip())
